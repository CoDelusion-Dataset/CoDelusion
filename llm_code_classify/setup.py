#!/usr/bin/env python3
"""Setup script for the LLM Code Hallucination Detection package."""

from setuptools import setup, find_packages


# Read long description from README.md
with open('README.md', 'r', encoding='utf-8') as f:
    long_description = f.read()

# Read requirements from requirements.txt
with open('requirements.txt', 'r', encoding='utf-8') as f:
    requirements = f.read().splitlines()

setup(
    name="llm-hallucination-detect",
    version="0.1.0",
    description="Tool for detecting hallucinations in code generated by LLMs",
    long_description=long_description,
    long_description_content_type="text/markdown",
    author="",
    author_email="",
    url="xxxxxx",
    packages=find_packages(),
    classifiers=[
        "Programming Language :: Python :: 3",
        "License :: OSI Approved :: MIT License",
        "Operating System :: OS Independent",
        "Development Status :: 3 - Alpha",
        "Intended Audience :: Developers",
        "Topic :: Software Development :: Quality Assurance",
        "Topic :: Scientific/Engineering :: Artificial Intelligence",
    ],
    python_requires=">=3.8",
    install_requires=requirements,
    entry_points={
        "console_scripts": [
            "hallucination-detect=scripts.detect:main",
            "hallucination-evaluate=scripts.evaluate:main",
        ],
    },
) 